package domain.player;

/**
 *
 * @author arace
 */
public class Player {
    String name;
    int score;

    public Player() {
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public int getScore() {
        return score;
    }

    public void setScore(int score) {
        this.score = score;
    }

    @Override
    public String toString() {
        return "Jugador{" + "name=" + name + ", score=" + score + '}';
    }
    
    
}
