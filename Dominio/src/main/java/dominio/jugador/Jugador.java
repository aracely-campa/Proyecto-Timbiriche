package dominio.jugador;

/**
 *
 * @author arace
 */
public class Jugador {
    String nombre;
    int score;

    public Jugador() {
    }

    public String getNombre() {
        return nombre;
    }

    public void setNombre(String nombre) {
        this.nombre = nombre;
    }

    public int getScore() {
        return score;
    }

    public void setScore(int score) {
        this.score = score;
    }

    @Override
    public String toString() {
        return "Jugador{" + "nombre=" + nombre + ", score=" + score + '}';
    }
    
    
}
